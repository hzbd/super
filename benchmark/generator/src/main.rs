use clap::{Parser, ValueEnum};
use std::fs;
use std::path::PathBuf;

#[derive(Parser)]
struct Args {
    #[arg(long, default_value = "50")]
    count: usize,

    #[arg(long, default_value = "idle")]
    mode: String,

    #[arg(long)]
    payload_path: PathBuf,

    #[arg(long)]
    output_dir: PathBuf,
}

fn main() -> anyhow::Result<()> {
    let args = Args::parse();
    fs::create_dir_all(&args.output_dir)?;

    let payload_bin = args.payload_path.canonicalize()?.to_string_lossy().to_string();

    // 1. Generate Super (TOML)
    let mut super_conf = String::from("# Generated by super-benchmark\n\n");
    for i in 0..args.count {
        super_conf.push_str(&format!(
            "[[program]]\n\
            name = \"bench-{i}\"\n\
            command = \"{bin}\"\n\
            args = [\"--mode\", \"{mode}\"]\n\
            autostart = true\n\
            retry_limit = 3\n\n",
            i = i, bin = payload_bin, mode = args.mode
        ));
    }
    fs::write(args.output_dir.join("super.toml"), super_conf)?;

    // 2. Generate Supervisor (INI)
    let mut supervisor_conf = String::from("[supervisord]\nnodaemon=false\nlogfile=/tmp/supervisord.log\npidfile=/tmp/supervisord.pid\n\n");
    // HTTP server required for supervisorctl; count as baseline overhead for fair comparison
    supervisor_conf.push_str("[inet_http_server]\nport=127.0.0.1:9001\n\n[rpcinterface:supervisor]\nsupervisor.rpcinterface_factory = supervisor.rpcinterface:make_main_rpcinterface\n\n[supervisorctl]\nserverurl=http://127.0.0.1:9001\n\n");

    for i in 0..args.count {
        supervisor_conf.push_str(&format!(
            "[program:bench-{i}]\n\
            command={bin} --mode {mode}\n\
            autostart=true\n\
            autorestart=true\n\
            startretries=3\n\n",
            i = i, bin = payload_bin, mode = args.mode
        ));
    }
    fs::write(args.output_dir.join("supervisord.conf"), supervisor_conf)?;

    // 3. Generate PM2 (JS Ecosystem)
    let mut pm2_apps = Vec::new();
    for i in 0..args.count {
        pm2_apps.push(format!(
            "    {{\n\
                  name: 'bench-{i}',\n\
                  script: '{bin}',\n\
                  args: '--mode {mode}',\n\
                  instances: 1,\n\
                  autorestart: true,\n\
                  max_restarts: 3\n\
                }}",
            i = i, bin = payload_bin, mode = args.mode
        ));
    }
    let pm2_conf = format!("module.exports = {{\n  apps : [\n{}\n  ]\n}};", pm2_apps.join(",\n"));
    fs::write(args.output_dir.join("ecosystem.config.js"), pm2_conf)?;

    println!("Configs generated in {:?}", args.output_dir);
    Ok(())
}
